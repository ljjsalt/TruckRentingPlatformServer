CREATE TABLE USER(

  ID INTEGER PRIMARY KEY ,
  USERNAME TEXT NOT NULL ,
  PASSWORD TEXT NOT NULL ,

  NICKNAME TEXT ,
  HEAD TEXT ,

  HISTORY_ORDER TEXT ,
  PRESENT_ORDER TEXT ,

  DC TEXT ,  -- discount card
  AP INTEGER ,  -- accumulate points

  DRIVERS TEXT
);



CREATE TABLE DRIVER(

  ID INTEGER PRIMARY KEY ,
  DRIVERNAME TEXT NOT NULL ,
  PASSWORD TEXT NOT NULL ,

  NICKNAME TEXT ,
  HEAD TEXT ,

  HISTORY_ORDER TEXT,
  PRESENT_ORDER TEXT
);



CREATE TABLE "GOOD"(

  ID INTEGER PRIMARY KEY ,
  USERID INTEGER NOT NULL ,
  DRIVERID INTEGER NOT NULL ,
  START_TIME REAL  NOT NULL ,
  END_TIME REAL ,
  REMARK TEXT
);

